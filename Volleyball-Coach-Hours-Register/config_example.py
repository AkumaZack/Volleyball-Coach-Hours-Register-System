# 使用 Email 通知設定範例
# 把這個檔案複製一份改名成 config.py，然後編輯裡面的 Email 與密碼

# 後台登入密碼
ADMIN_KEY = "change_this_to_your_admin_key"

# Email 設定
EMAIL_SMTP_SERVER = "smtp.gmail.com"
EMAIL_SMTP_PORT = 587

# 你的 Gmail 帳號
EMAIL_USERNAME = "your_email@gmail.com"

# Gmail 應用程式密碼（不是登入密碼）
EMAIL_PASSWORD = "your_app_password"

# 要把通知寄給誰（通常就是你自己）
EMAIL_TO = "your_email@gmail.com"

